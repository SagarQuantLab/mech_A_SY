# TUPLE
# (), ordered, index, allowed duplicates, non-mutable

my_tuple = (1, 3, 2, 5, 6, 5)

# access first element
print(my_tuple[0])

# access second element
print(my_tuple[1])

# access first three elements
print(my_tuple[0:3:1])

# access only first and third element
print(my_tuple[0:3:2])

# tuple print
print(my_tuple)

